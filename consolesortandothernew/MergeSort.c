/*
 * СОРТИРОВКА СЛИЯНИЕМ (Merge Sort)
 * 
 * Принцип работы (алгоритм "разделяй и властвуй"):
 * 1. Разделяем массив пополам до получения массивов из 1 элемента
 * 2. Сливаем отсортированные части обратно в порядке возрастания
 * 
 * Сложность: O(n log n) во всех случаях
 * Стабильная сортировка, но требует дополнительной памяти O(n)
 */

#include <stdio.h>
#include <stdlib.h>
#include <locale.h>
#include <windows.h>

// Функция слияния двух отсортированных подмассивов
void merge(int arr[], int l, int m, int r) {
    int n1 = m - l + 1;     // Размер левого подмассива
    int n2 = r - m;         // Размер правого подмассива
    
    // Создаем временные массивы для левой и правой частей
    int* L = (int*)malloc(n1 * sizeof(int));
    int* R = (int*)malloc(n2 * sizeof(int));
    
    // Копируем данные во временные массивы
    for (int i = 0; i < n1; i++)
        L[i] = arr[l + i];          // Левая часть
    for (int j = 0; j < n2; j++)
        R[j] = arr[m + 1 + j];      // Правая часть
    
    // Слияние временных массивов обратно в arr[l..r]
    int i = 0, j = 0, k = l;   // Индексы для L[], R[], arr[]
    
    // Основной цикл слияния: сравниваем элементы и выбираем меньший
    while (i < n1 && j < n2) {
        if (L[i] <= R[j]) {
            arr[k] = L[i];     // Берем из левого массива
            i++;
        } else {
            arr[k] = R[j];     // Берем из правого массива
            j++;
        }
        k++;
    }
    
    // Копируем оставшиеся элементы левого массива (если есть)
    while (i < n1) {
        arr[k] = L[i];
        i++;
        k++;
    }
    
    // Копируем оставшиеся элементы правого массива (если есть)
    while (j < n2) {
        arr[k] = R[j];
        j++;
        k++;
    }
    
    // Освобождаем выделенную память
    free(L);
    free(R);
}

// Основная рекурсивная функция сортировки слиянием
void mergeSort(int arr[], int l, int r) {
    if (l < r) {  // Базовый случай: если есть более одного элемента
        // Находим середину, чтобы разделить массив на две части
        int m = l + (r - l) / 2;  // Защита от переполнения
        
        // Рекурсивно сортируем левую половину
        mergeSort(arr, l, m);
        // Рекурсивно сортируем правую половину
        mergeSort(arr, m + 1, r);
        
        // Сливаем отсортированные половины
        merge(arr, l, m, r);
    }
}

void printArray(int arr[], int size) {
    for (int i = 0; i < size; i++)
        printf("%d ", arr[i]);
    printf("\n");
}

int main() {
    SetConsoleCP(1251); // либо CP_UTF8
    SetConsoleOutputCP(1251);
    
    printf("=== Сортировка слиянием ===\n");
    int arr[] = {64, 34, 25, 12, 22, 11, 90};
    int n = sizeof(arr) / sizeof(arr[0]);
    
    printf("Исходный массив: ");
    printArray(arr, n);
    
    mergeSort(arr, 0, n - 1);
    
    printf("Отсортированный массив: ");
    printArray(arr, n);
    
    return 0;
} 